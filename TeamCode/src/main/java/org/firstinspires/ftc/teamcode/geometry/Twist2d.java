package org.firstinspires.ftc.teamcode.geometry;

public class Twist2d {
    public final double dX;
    public final double dY;
    public final double dTheta;

    public Twist2d(double dX, double dY, double dTheta) {
        this.dX = dX;
        this.dY = dY;
        this.dTheta = dTheta;
    }
}